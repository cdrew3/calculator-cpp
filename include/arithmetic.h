namespace arithmetic {

    bool equal(int, int);
    bool equal(float, float);

    // Addition
    int add(int x, int y) {
        return x + y;
    }
    float add(float x, float y) {
        return x + y;
    }

    // Subtraction
    int sub(int x, int y) {
        return x - y;
    }
    float sub(float x, float y) {
        return x - y;
    }

    // Multiply
    int mul(int x, int y) {
        return x * y;
    }
    float mul(float x, float y) {
        return x * y;
    }

    // Divide
    int div(int x, int y) {
        return x / y;
    }
    float div(float x, float y) {
        return x / y;
    }
}